import turtle

turtle.shape('turtle')


for r in range(10,110,10):
    

    

    turtle.up()

    turtle.forward(5)

    turtle.right(90)

    turtle.forward(5)

    turtle.right(180)

    turtle.down()

    turtle.forward(r)
    turtle.left(90)
    turtle.forward(r)
    turtle.left(90)
    turtle.forward(r)
    turtle.left(90)
    turtle.forward(r)



